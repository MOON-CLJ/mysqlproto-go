package mysqlproto

import (
	"bytes"
	"testing"

	"github.com/stretchr/testify/assert"
)

func TestReadPacket7(t *testing.T) {
	stream := bytes.NewBuffer([]byte{
		0x07, 0x00, 0x00, 0x02,
		0x00, 0x00, 0x00, 0x02,
		0x01, 0x02, 0x03,
	})

	proto := NewProto()
	packet, err := proto.ReadPacket(stream)
	assert.Nil(t, err)
	assert.Equal(t, packet.SequenceID, byte(0x02))
	assert.Len(t, packet.Payload, 7)
	assert.Equal(t, packet.Payload, []byte{0x00, 0x00, 0x00, 0x02, 0x01, 0x02, 0x03})
}

